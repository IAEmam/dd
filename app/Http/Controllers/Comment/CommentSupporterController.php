    public function approveComment(Comment $comment)
    {
        if ($comment->status) {
            $comment->update([
                'supporter_id' => auth()->user()->id,
            ]);
            $comment->status = true;

        }
        return response()->json($comment);
    }

    public function disapproveComment(Comment $comment)
    {
        $comment->delete();
        return response()->json($comment);
    }
